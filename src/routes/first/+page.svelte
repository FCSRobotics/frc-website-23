  <h2>What is FIRST?</h2>
  <p>
    FIRST is a global robotics organization that aims to prepare students to
    become future leaders in science and technology. The acronym FIRST stands
    for “For the Inspiration and Recognition of Science and Technology”. Through
    various competitions including FIRST Lego League, FIRST Tech Challenge, and
    FIRST Robotics Competition, students of all grades K-12 have an opportunity
    to work with skilled adult mentors in a fast-paced, collaborative
    environment in order to solve real world technological challenges through
    building robots. FIRST’s mission is to inspire innovation and foster
    well-rounded, self-confident student-leaders. <a
      href="https://www.firstinspires.org/">Click here to read more.</a
    >
  </p>
  <h2>What is FIRST Robotics Competition?</h2>
  <p>
    FIRST Robotics Competition (FRC) is the high school level competition which
    the Earthquakers compete in. It is also the oldest and most technologically
    advanced competition. Teams spend about eight weeks from January to
    February, building and programming a robot that can weigh about 125 lbs and
    be almost as tall as a person. Along the way students collaborate with
    mentors to learn 3D design and CAD skills, programming, and machining. The
    Earthquakers compete in the FIRST Midatlantic District (FMA) and attend two
    regional competitions per year along with the district championship in April
    if we qualify. <a href="https://www.firstinspires.org/robotics/frc"
      >Click here to learn more about FIRST Robotics Competition.</a
    >
  </p>
  <h2>What else should you know?</h2>
  <p>
    Although all FIRST competitions involve building robots, the organization is
    primarily about promoting science and technology amongst students and the
    community at large. FIRST was started by Dean Kamen, an entrepreneur and
    engineer, because he wanted to create a pipeline of future STEM leaders
    through the hands-on challenge of robot building. FIRST teams try to live up
    to this vision by having strong outreach and community service programs in
    addition to building robots. Promoting the joy and excitement that science
    brings is just as important as building the robot itself.
  </p>
