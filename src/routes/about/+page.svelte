  <div id="cid-text">
    <h2>About the Center for Innovation and Design</h2>
    <p>
      Envisioned by our current head of school Beth Johnson, The Center for
      Innovation and Design (CID) is an exciting architectural project that will
      transform the Rex Gymnasium on Friends’ Central School’s upper campus into
      a state-of-the-art makerspace. The first phase of the CID began in the
      fall of 2022 and the final construction of the CID will be completed by
      the start of the 2024-25 school year. The Earthquakers were created by a
      group of enthusiastic students and mentors as part of the mission of the
      CID to promote design thinking, incorporate advanced technology, and
      integrate hands-on making into the Friends’ Central curriculum so that we
      can remain a leader in 21st century education.
    </p>
